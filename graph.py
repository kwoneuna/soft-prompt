def deduplicate_templates(templates, threshold=80):
    """
    문자열 유사도가 threshold 이상이면 같은 그룹으로 보고 하나만 남김
    """
    unique_templates = []
    for t in templates:
        if not any(fuzz.ratio(t, u) >= threshold for u in unique_templates):
            unique_templates.append(t)
    return unique_templates

